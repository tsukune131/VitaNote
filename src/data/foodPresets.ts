function parse(raw: string): FoodPreset[] {
  const out: FoodPreset[] = [];
  let cat = '';
  for (const line of raw.split('\n')) {
    const t = line.trim();
    if (!t) continue;
    if (t.startsWith('#')) {
      cat = t.slice(1).trim();
      continue;
    }
    const [name, kana, kcal, unit] = t.split('\t');
    if (!name || !kcal) continue;
    out.push({ name, kana: kana || name, kcal: Number(kcal), unit: unit || '1人前', cat });
  }
  return out;
}

